/**
 * config/jwt.js - JWT（JSON Web Token）配置文件
 *
 * 这个文件负责JWT的密钥和过期时间配置
 *
 * 什么是JWT？
 * JWT是一种身份认证方式，类似"入场券"
 * - 用户登录成功 → 服务器发一张"入场券"（token）
 * - 后续每次请求 → 用户带上"入场券" → 服务器验证"入场券"真假
 * - "入场券"过期了 → 用户需要重新登录
 *
 * 为什么单独拆出来？
 * 因为JWT密钥可能被多个地方使用（登录、验证token等）
 * 如果写死在代码里，修改时要找半天
 */

// ============================================
// JWT 密钥
// ============================================

/**
 * jwtSecret：JWT签名密钥
 *
 * 什么是签名？
 * 服务器用这个密钥对token进行"签名"
 * 就像在入场券上盖章，防止伪造
 *
 * 为什么需要密钥？
 * - 没有密钥，任何人都能伪造token
 * - 有了密钥，只有服务器能生成和验证token
 *
 * 重要：这个密钥必须保密！不能泄露给任何人！
 * 生产环境应该放在环境变量里，不要写在代码里
 */
const jwtSecret = 'ai-chat-jwt-secret-key-2026';

// ============================================
// JWT 过期时间
// ============================================

/**
 * jwtExpiresIn：token过期时间
 *
 * '24h' = 24小时后过期
 * 过期后，用户需要重新登录
 *
 * 为什么设置过期时间？
 * 1. 安全：token被偷了，最多用24小时
 * 2. 体验：用户不用频繁登录
 *
 * 其他选项：
 * - '1h'：1小时过期（更安全，但要频繁登录）
 * - '7d'：7天过期（更方便，但安全性降低）
 * - '30d'：30天过期（适合移动端APP）
 */
const jwtExpiresIn = '24h';

// ============================================
// 导出配置
// ============================================

module.exports = {
  jwtSecret,
  jwtExpiresIn
};
