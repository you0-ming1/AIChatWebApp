<script setup lang="ts">
/**
 * App.vue - 根组件
 * 
 * ============================================
 * 文件作用
 * ============================================
 * 
 * 这是整个应用的根组件
 * 所有页面组件都是它的子组件
 * 
 * ============================================
 * 页面结构
 * ============================================
 * 
 * App.vue 只包含一个 <router-view>
 * 
 * <router-view> 是Vue Router提供的占位符
 * 根据当前URL，渲染对应的页面组件
 * 
 * 比如：
 * - 访问 /login → <router-view> 变成 LoginView.vue
 * - 访问 /chat → <router-view> 变成 ChatView.vue
 * 
 * ============================================
 * 设计思路
 * ============================================
 * 
 * 为什么App.vue这么简单？
 * 
 * 因为Vue是单页应用（SPA）
 * 整个应用只有一个HTML页面
 * 不同"页面"只是替换了部分内容
 * 
 * App.vue就是那个"容器"
 * <router-view>就是"内容区"
 * 路由切换时，只是替换<router-view>的内容
 * 不会重新加载整个页面
 * 
 * 好处：
 * 1. 切换更快（不需要重新加载）
 * 2. 体验更好（无白屏闪烁）
 * 3. 状态可以保留（比如输入框内容）
 */
</script>

<template>
  <!-- 
    router-view：路由视图
    
    根据当前URL渲染对应的页面组件
    
    比如当前URL是 /chat：
    router-view 会渲染 ChatView.vue
    
    URL变化时：
    router-view 会自动替换为新的组件
  -->
  <router-view />
</template>

<style scoped>
/* 
 * scoped样式
 * 
 * 加了scoped的样式只在当前组件生效
 * 不会影响其他组件
 * 
 * 比如这里的样式不会影响 LoginView.vue
 */
</style>
